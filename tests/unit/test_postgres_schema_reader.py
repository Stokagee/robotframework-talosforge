"""
Unit tests for PostgreSQL schema reader.

Tyto testy ověřují správnost mapování PostgreSQL typů na JSON Schema,
detekci auto-generated sloupců a další funkčnost.
"""

import pytest

from TalosForge.db.postgres_schema_reader import PostgresSchemaReader, TYPE_MAP


class TestTypeMapCoverage:
    """Testy pro pokrytí mapování PostgreSQL typů."""

    def test_integer_types_mapped(self):
        """Test, že integer typy jsou mapovány správně."""
        assert TYPE_MAP['integer']['type'] == 'integer'
        assert TYPE_MAP['bigint']['type'] == 'integer'
        assert TYPE_MAP['smallint']['type'] == 'integer'
        assert TYPE_MAP['int2']['type'] == 'integer'
        assert TYPE_MAP['int4']['type'] == 'integer'
        assert TYPE_MAP['int8']['type'] == 'integer'

    def test_numeric_types_mapped(self):
        """Test, že numerické typy jsou mapovány na 'number'."""
        assert TYPE_MAP['numeric']['type'] == 'number'
        assert TYPE_MAP['decimal']['type'] == 'number'
        assert TYPE_MAP['real']['type'] == 'number'
        assert TYPE_MAP['double precision']['type'] == 'number'

    def test_string_types_mapped(self):
        """Test, že string typy jsou mapovány správně."""
        assert TYPE_MAP['varchar']['type'] == 'string'
        assert TYPE_MAP['character varying']['type'] == 'string'
        assert TYPE_MAP['text']['type'] == 'string'

    def test_boolean_type_mapped(self):
        """Test, že boolean typ je mapován správně."""
        assert TYPE_MAP['boolean']['type'] == 'boolean'
        assert TYPE_MAP['bool']['type'] == 'boolean'

    def test_datetime_types_with_format(self):
        """Test, že date/time typy mají správný formát."""
        assert TYPE_MAP['date']['type'] == 'string'
        assert TYPE_MAP['date']['format'] == 'date'

        assert TYPE_MAP['timestamp']['type'] == 'string'
        assert TYPE_MAP['timestamp']['format'] == 'date-time'

        assert TYPE_MAP['timestamptz']['type'] == 'string'
        assert TYPE_MAP['timestamptz']['format'] == 'date-time'

    def test_uuid_type_with_format(self):
        """Test, že UUID typ má správný formát."""
        assert TYPE_MAP['uuid']['type'] == 'string'
        assert TYPE_MAP['uuid']['format'] == 'uuid'

    def test_json_types_mapped(self):
        """Test, že JSON typy jsou mapovány na object."""
        assert TYPE_MAP['json']['type'] == 'object'
        assert TYPE_MAP['jsonb']['type'] == 'object'

    def test_binary_type_with_encoding(self):
        """Test, že binary typ má správné kódování."""
        assert TYPE_MAP['bytea']['type'] == 'string'
        assert TYPE_MAP['bytea']['contentEncoding'] == 'base64'


class TestAutoGeneratedDetection:
    """Testy pro detekci auto-generated sloupců."""

    def test_identity_column_detected(self):
        """Test detekce IDENTITY sloupce."""
        assert PostgresSchemaReader._is_auto_generated(None, 'YES') is True
        assert PostgresSchemaReader._is_auto_generated("some_default", 'YES') is True

    def test_serial_column_detected(self):
        """Test detekce SERIAL sloupce pomocí nextval."""
        assert PostgresSchemaReader._is_auto_generated("nextval('users_id_seq')", 'NO') is True
        assert PostgresSchemaReader._is_auto_generated("nextval('seq')", 'NO') is True

    def test_regular_column_not_detected(self):
        """Test, že běžný sloupec není detekován jako auto-generated."""
        assert PostgresSchemaReader._is_auto_generated(None, 'NO') is False
        assert PostgresSchemaReader._is_auto_generated("'default_value'", 'NO') is False


