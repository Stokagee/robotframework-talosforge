class TestColumnDescription:
    """Testy pro přidání description do JSON Schema."""

    def test_description_added_to_schema(self):
        """Test, že description se přidá do JSON Schema když existuje."""
        reader = PostgresSchemaReader(dsn="host=localhost")
        result = reader._map_type_to_json_schema(
            udt_name='varchar',
            column_name='email',
            is_nullable='YES',
            column_default=None,
            is_identity='NO',
            column_description='User email address'
        )
        assert result is not None
        assert result['type'] == 'string'
        assert 'description' in result
        assert result['description'] == 'User email address'

    def test_description_none_omitted(self):
        """Test, že None description se nepřidá do JSON Schema."""
        reader = PostgresSchemaReader(dsn="host=localhost")
        result = reader._map_type_to_json_schema(
            udt_name='varchar',
            column_name='email',
            is_nullable='YES',
            column_default=None,
            is_identity='NO',
            column_description=None
        )
        assert result is not None
        assert result['type'] == 'string'
        assert 'description' not in result

    def test_description_with_auto_generated_column(self):
        """Test, že description se nepřidá pro auto-generated sloupce (vždy readOnly)."""
        reader = PostgresSchemaReader(dsn="host=localhost")
        result = reader._map_type_to_json_schema(
            udt_name='integer',
            column_name='id',
            is_nullable='NO',
            column_default=None,
            is_identity='YES',
            column_description='Unique identifier'
        )
        # Auto-generated sloupce vrací specifický schema bez description
        assert result is not None
        assert result['type'] == 'integer'
        assert result['readOnly'] is True
        assert result['x-system-generated'] is True
        # Description se nepřidává pro auto-generated (můžeme změnit později)

    def test_description_with_integer_type(self):
        """Test, že description funguje pro integer typ."""
        reader = PostgresSchemaReader(dsn="host=localhost")
        result = reader._map_type_to_json_schema(
            udt_name='integer',
            column_name='age',
            is_nullable='YES',
            column_default=None,
            is_identity='NO',
            column_description='User age in years'
        )
        assert result is not None
        assert result['type'] == 'integer'
        assert result['description'] == 'User age in years'


