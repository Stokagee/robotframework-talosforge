class TestPostgresSchemaReaderInit:
    """Testy pro inicializaci PostgresSchemaReader."""

    def test_init_requires_psycopg2(self, monkeypatch):
        """Test, že inicializace selže bez psycopg2."""
        # Simulace chybějícího psycopg2
        import TalosForge.db.postgres_schema_reader as reader_module
        monkeypatch.setattr(reader_module, 'PSYCOPG2_AVAILABLE', False)

        from TalosForge.core.exceptions import TalosForgeException

        with pytest.raises(TalosForgeException) as exc_info:
            PostgresSchemaReader(
                dsn="host=localhost",
                schema="public",
                table="test"
            )

        assert "psycopg2 is required" in str(exc_info.value)

    def test_init_with_default_values(self):
        """Test inicializace s výchozími hodnotami."""
        if not PostgresSchemaReader.__module__:
            pytest.skip("psycopg2 not available")

        reader = PostgresSchemaReader(
            dsn="host=localhost",
        )
        assert reader.schema == "public"
        assert reader.table == ""
        assert reader.exclude_columns == []


